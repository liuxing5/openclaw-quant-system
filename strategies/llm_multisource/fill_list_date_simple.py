def infer_list_date(code):
    if code in KNOWN_LIST_DATES:
        return pd.to_datetime(KNOWN_LIST_DATES[code]).date()
    return None

def batch_fill_list_date(limit=1000):
    conn = None
    try:
        conn = get_db_fresh()
        cur = conn.cursor()

        cur.execute('''
            SELECT ts_code FROM stock_basic_info WHERE list_date IS NULL LIMIT %s;
        ''', (limit,))
        missing = [row[0] for row in cur.fetchall()]

        if not missing:
            print("✅ 没有需要填充的股票")
            return 0

        print(f"开始填充 {len(missing)} 只股票的上市日期...")
        success = 0
        known_count = 0
        infer_count = 0

        for i, ts_code in enumerate(missing, 1):
            list_date = None

            if ts_code in KNOWN_LIST_DATES:
                list_date = pd.to_datetime(KNOWN_LIST_DATES[ts_code]).date()
                known_count += 1
                method = '已知'

            if not list_date:
                list_date = infer_list_date(ts_code)
                if list_date:
                    infer_count += 1
                    method = '推断'

            if list_date:
                cur.execute('''
                    UPDATE stock_basic_info SET list_date = %s WHERE ts_code = %s;
                ''', (list_date, ts_code))
                success += 1

                if i % 50 == 0:
                    conn.commit()
                    print(f"  进度: {i}/{len(missing)}")

        conn.commit()
        cur.close()

        print(f"\n📊 完成: 成功 {success}/{len(missing)}")
        print(f"   - 已知日期: {known_count}")
        print(f"   - 推断日期: {infer_count}")

        return success
    except Exception as e:
        print(f"batch_fill_list_date 失败: {e}")
        return 0
    finally:
        if conn and not conn.closed:
            conn.close()

if __name__ == '__main__':
    success_count = batch_fill_list_date(limit=2000)
    print(f"\n✅ 本次填充 {success_count} 条上市日期")
